/* Captura directa de /gerente como GERENTE (alerta caja +24h) */
const { chromium } = require('playwright')

;(async () => {
  const browser = await chromium.launch()
  const page = await browser.newPage({ viewport: { width: 1440, height: 900 } })
  await page.goto('http://localhost:3000/login', { waitUntil: 'networkidle' })
  await page.fill('input[type="email"]', 'kevin.sandoval@quinuapata.com')
  await page.fill('input[type="password"]', 'Quinuapata2026!')
  await page.click('button[type="submit"]')
  await page.waitForURL(u => !String(u).includes('/login'), { timeout: 20000 })
  await page.waitForTimeout(3000)
  await page.getByRole('link', { name: 'Gerencial' }).click()
  await page.waitForURL('**/gerente', { timeout: 15000 })
  await page.waitForTimeout(4000)
  await page.screenshot({ path: 'pre-gerencial-light.png', fullPage: true })
  await page.evaluate(() => document.documentElement.classList.add('dark'))
  await page.waitForTimeout(400)
  await page.screenshot({ path: 'pre-gerencial-dark.png', fullPage: true })
  await browser.close()
  console.log('OK')
})().catch(e => { console.error(e); process.exit(1) })
